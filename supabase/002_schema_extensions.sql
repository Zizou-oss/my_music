-- ---------------------------------------------------------------------------
-- Read-only dashboard view on materialized stats
-- ---------------------------------------------------------------------------
create or replace view public.admin_song_stats_daily as
select
  d.day,
  d.song_id,
  s.title,
  s.artist,
  d.plays_count,
  d.listeners_count,
  d.seconds_total
from public.mv_song_stats_daily d
join public.songs s on s.id = d.song_id
where public.is_admin()
order by d.day desc, d.plays_count desc;

